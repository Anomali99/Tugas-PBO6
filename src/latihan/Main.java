    private void tampilkan(){
        PreparedStatement st = null;
        ArrayList<Santri> list = new ArrayList();
        ResultSet rs = null;
        String query = "SELECT * FROM santri ";
        try{
            st = conn.prepareStatement(query);
            rs = st.executeQuery();
            while(rs.next()){
                Santri sn = new Santri();
                
                sn.setId(rs.getString("id_santri"));
                sn.setNama(rs.getString("nama"));
                sn.setJk(rs.getString("jenis_kelamin"));
                sn.setAlamat(rs.getString("alamat"));
                sn.setTlp(rs.getString("no_tlp"));
                sn.setUsia(Integer.parseInt(rs.getString("usia")));
                
                list.add(sn);
            }
            
            DefaultTableModel tbl = (DefaultTableModel) tbl_isi.getModel();
            tbl.setRowCount(0);
            int i = 1;
            for(Santri data : list){
                Object[] baris = new Object[6];
                
                baris[0] = data.getId();
                baris[1] = data.getNama();
                baris[2] = data.getJk();
                baris[3] = data.getAlamat();
                baris[4] = data.getUsia();
                baris[5] = data.getTlp();
                
                tbl.addRow(baris);
            }
        } catch (SQLException e){
            e.printStackTrace();
        }
        btn_hapus.setVisible(false);
        btn_batal.setVisible(false);
        if(lb_tambah.getText().equals("UBAH")){
            lb_tambah.setText("TAMBAH");
        }
    }
    
    private void hapus(String id){
        String query = "DELETE FROM santri WHERE id_santri = ?";
        try{
            PreparedStatement stmt = null;
            stmt = conn.prepareStatement(query);
            
            stmt.setString(1, id);
              
            stmt.executeUpdate();
        } catch (SQLException e){
            e.printStackTrace();
        }
    }
    private void setColWidht() {
        int[] noCol = {0,1,2,4,5};
        int[] noColW = {40,200,30,40,150};
        
        TableColumnModel tblModel = tbl_isi.getColumnModel();
        for(int i = 0; i < noCol.length; i++){
        tblModel.getColumn(noCol[i]).setPreferredWidth(noColW[i]);
        tblModel.getColumn(noCol[i]).setMaxWidth(noColW[i]);
        tblModel.getColumn(noCol[i]).setMinWidth(noColW[i]);
        }
    }

