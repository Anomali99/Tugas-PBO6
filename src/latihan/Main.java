    private void btn_tambahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_tambahMouseClicked
        try {
            if(lb_tambah.getText().equalsIgnoreCase("TAMBAH")){
                new Tambah(null,true).setVisible(true);
            } else {
                int index = tbl_isi.getSelectedRow();
                Santri sn = new Santri();
                sn.setId((String) tbl_isi.getValueAt(index, 0));
                sn.setNama((String) tbl_isi.getValueAt(index, 1));
                sn.setJk((String) tbl_isi.getValueAt(index, 2));
                sn.setAlamat((String) tbl_isi.getValueAt(index, 3));
                sn.setUsia((int) tbl_isi.getValueAt(index, 4));
                sn.setTlp((String) tbl_isi.getValueAt(index, 5));
                new Tambah(null,true,sn).setVisible(true);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        tampilkan();
    }//GEN-LAST:event_btn_tambahMouseClicked

